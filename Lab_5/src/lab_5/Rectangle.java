package lab_5;

public class Rectangle {

    int highet;
    int width;

    public Rectangle() {
    }

    public Rectangle(int highet, int width) {
        this.highet = highet;
        this.width = width;
    }

    public int getHighet() {
        return highet;
    }

    public void setHighet(int highet) {
        this.highet = highet;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

   
}
