package lab.pkg8;

/**
 *
 * @author lab
 */
public class Author {
    private String name ;
    private int age ;
    private String address ;

    public Author() {
        name = null ;
        age = 0 ;
        address = null ;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }
    
    
}
